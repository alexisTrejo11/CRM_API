package at.backend.CRM.MarketingProject.Utils.Enums;

public enum MetricType {
    COUNT,          // For impressions, clicks, leads, etc.
    PERCENTAGE,     // For conversion rates, bounce rates, etc.
    CURRENCY,       // For revenue, profit, etc.
    RATIO,          // For cost per acquisition, etc.
    DURATION,       // For time on page, etc.
    COST,           // For cost per click, etc.
    SCORE,          // For NPS, satisfaction scores, etc.
    CUSTOM          // For custom metrics
}
